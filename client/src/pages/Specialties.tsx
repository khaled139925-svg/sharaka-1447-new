export default function Specialties({ onNavigate }: SpecialtiesProps) {

  const [openIndex, setOpenIndex] = useState<number | null>(null);

  const toggle = (index: number) => {
    if (openIndex === index) {
      setOpenIndex(null);
    } else {
      setOpenIndex(index);
    }
  };

  const goHome = () => {
    if (onNavigate) onNavigate("home");
    else window.location.href = "/";
  };

  return (
    <div className="min-h-screen bg-gray-50 py-20 px-6" dir="rtl">

      <div className="max-w-5xl mx-auto space-y-10">

        <button
          onClick={goHome}
          className="text-[#1976D2] hover:text-[#FF9800] text-lg font-semibold"
        >
          ← العودة للرئيسية
        </button>

        <h1 className="text-4xl font-bold text-center text-[#FF9800]">
          التخصصات
        </h1>

        <div className="space-y-4">

          {SPECIALTIES.map((spec, index) => (

            <div
              key={index}
              className="bg-white rounded-xl shadow-lg"
            >

              <button
                onClick={() => toggle(index)}
                className="w-full flex justify-between items-center p-6 text-xl font-bold text-[#1976D2]"
              >
                {spec.title}

                <span>
                  {openIndex === index ? "▲" : "▼"}
                </span>

              </button>

              {openIndex === index && (

                <div className="px-8 pb-6 space-y-2 text-gray-700">

                  {spec.items.map((item, i) => (

                    <div key={i} className="py-1">
                      • {item}
                    </div>

                  ))}

                </div>

              )}

            </div>

          ))}

        </div>

      </div>

    </div>
  );
}